<?php

$a = 10;
$b = 3;

// Comparações
echo "a > b: " . ($a > $b ? "true" : "false") . "<br>";
echo "a < b: " . ($a < $b ? "true" : "false") . "<br>";
echo "a >= b: " . ($a >= $b ? "true" : "false") . "<br>";
echo "a <= b: " . ($a <= $b ? "true" : "false") . "<br>";

// Operador de resto (%)
echo "a % b: " . ($a % $b);

?>