<?php

$numero = 5;

// Incremento
echo "Valor inicial: " . $numero . "<br>";
$numero++;
echo "Depois do ++: " . $numero . "<br>";

// Decremento
$numero--;
echo "Depois do --: " . $numero . "<br>";

// Pré-incremento
echo "Pré-incremento: " . ++$numero . "<br>";

// Pós-incremento
echo "Pós-incremento: " . $numero++ . "<br>";
echo "Valor final: " . $numero;

?>